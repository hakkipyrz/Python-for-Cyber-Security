# And or not 

# And = tüm koşulların doğru olması gerekir true çıktısı vermesi için
# Or = yalnız 1 koşul doğru ise her zaman true çıktısı verir.

print(2<1 and 3>2) #false

print(2>1 and 3>2) #true

print(2<1 or 3>2) #true

print(not 10==10) #10 10a eşit değildir dedik o da false döndürdü